package com.netflox.beans;

public class Role {
	
	private String idDvd;
	private String idArtiste;
	private String role;
	
	
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public String getIdDvd() {
		return idDvd;
	}
	public void setIdDvd(String idDvd) {
		this.idDvd = idDvd;
	}
	public String getIdArtiste() {
		return idArtiste;
	}
	public void setIdArtiste(String idArtiste) {
		this.idArtiste = idArtiste;
	}

}
